print(mayor_igual)

# Menor o igual que
menor_igual = 5<=10
print("-- 5 es Menor o igual que 10 --")
print(menor_igual)

# Igual que
igual = "hola" == "hola"
print("-- 'hola' es Igual que 'hola' --")
print(igual)

# Diferente que
diferente = "hola" != "adios"
print("-- 'hola' es Diferente que 'adios' --")
print(diferente)