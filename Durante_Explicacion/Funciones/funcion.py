#Ejemplo de funciones 

#Funcion sin retorno
def saludar(nombre):
    print(f"Hola, {nombre}!")

#Funcion con retorno
def sumar(a, b):
    return a + b

#Funcion con parametros por defecto
def saludar_persona(nombre="Usuario"):
    print(f"Hola, {nombre}!")

