### Metodos aplicables a strings
#len()
#Devuelve la longitud de la cadena (número de caracteres).

# in 
#Verifica si una subcadena está presente en la cadena. Retorna True o False.

### Metodos de verificacion

#isalpha()
#Verifica si todos los caracteres de la cadena son alfabéticos.

#isdigit()
#Verifica si todos los caracteres de la cadena son dígitos.

#isalnum()
#Verifica si todos los caracteres de la cadena son alfanuméricos (letras y números).

#islower()
#Verifica si todos los caracteres de la cadena están en minúsculas.

#isupper()
#Verifica si todos los caracteres de la cadena están en mayúsculas.

#startswith(prefix)
#Verifica si la cadena comienza con el prefijo especificado.

#endswith(suffix)
#Verifica si la cadena termina con el sufijo especificado.

### Metodos de transformacion (retornan un nuevo string)

#lower()
#Convierte todos los caracteres de la cadena a minúsculas.

#upper()
#Convierte todos los caracteres de la cadena a mayúsculas.

#capitalize()
#Convierte el primer carácter de la cadena a mayúsculas y el resto a minúsculas.

#title()
#Convierte el primer carácter de cada palabra a mayúsculas y el resto a minúsculas.

#swapcase()
#Convierte los caracteres en mayúsculas a minúsculas y viceversa.

#strip()
#Elimina los espacios en blanco al principio y al final de la cadena.

#replace(old, new)
#Reemplaza todas las ocurrencias de una subcadena por otra.

### Metodos de busqueda y conteo

#find(sub)
#Devuelve el índice de la primera ocurrencia de una subcadena. Retorna -1 si no se encuentra.

#count(sub)
#Devuelve el número de veces que una subcadena aparece en la cadena.

#index(sub)
#Devuelve el índice de la primera ocurrencia de una subcadena. Lanza un error