# Ejemplo de concatenación de cadenas en Python

# Definimos dos cadenas
cadena_1 = "Hola"
cadena_2 = "Mundo"

# Concatenamos las cadenas
resultado = cadena_1 + " " + cadena_2

# Imprimimos el resultado
print("---- Resultado de la concatenación ----")
print(resultado)  # Imprime "Hola Mundo"

# Otra forma de concatenar
concatenacion = "Hola" + " " + "Mundo"
print("---- Resultado de la concatenación (otra forma) ----")
print(concatenacion)  # Imprime "Hola Mundo"
