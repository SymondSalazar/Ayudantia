cadena_repetida_1 = cadena_1 * 3
print("---- Cadena 1 repetida 3 veces ----")
print(cadena_repetida_1)  # Imprime "Hola, mundo!Hola, mundo!Hola, mundo!"

# Repetimos la cadena 2 dos veces
cadena_repetida_2 = cadena_2 * 2
print("---- Cadena 2 repetida 2 veces ----")
