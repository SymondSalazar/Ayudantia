#Esta sera las cadenas que vamos a repetir
cadena_1 = "Hola, mundo!"
cadena_2 = "Python es genial"

#Imprimimos las cadenas originales
print("---- Cadenas originales ----")
print(cadena_1)  # Imprime "Hola, mundo!"
print(cadena_2)  # Imprime "Python es genial"


