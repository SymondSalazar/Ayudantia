# Estructura de control iterativa: for

# Ejemplo de uso de for con una lista
Lista = [1, 2, 3, 4, 5]
print("---- Recorriendo una lista ----")
for elemento in Lista:
    print("Elemento:", elemento)

# Ejemplo de uso de for con un rango
print("---- Recorriendo un rango ----")
for i in range(5):
    print("Numero:", i)

# Ejemplo de uso de for recorriendo una cadena de caracteres
cadena = "Hola, mundo!"
print("---- Recorriendo una cadena de caracteres ----")
for caracter in cadena:
    print("Caracter:", caracter)
